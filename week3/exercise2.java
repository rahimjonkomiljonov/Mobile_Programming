public class exercise2 {
    public static void main(String[] args) {
        // addition
        float a = 56.2f;
        float b = 12.8f;
        float c = a + b;
        System.out.println("addition: c = " + c);

        // subtraction
        float g = 12.4f;
        float u = 1.8f;
        float d = g - u;
        System.out.println("subtraction: d = " + d);

        // multiplication
        float l = 2.2f;
        float k = 1.9f;
        float q = l * k;
        System.out.println("multiplication: q = " + q);

        // division
        float w = 6.2f;
        float dd = 2.1f;
        float i = w / dd;
        System.out.println("division: i = " + i);
    }
}
