public class exercise3 {
    public static void main(String[] args) {
        // addition
        double a = 56.2;
        double b = 12.8;
        double c = a + b;
        System.out.println("addition: c = " + c);

        // subtraction
        double g = 12.4;
        double u = 1.8;
        double d = g - u;
        System.out.println("subtraction: d = " + d);

        // multiplication
        double l = 2.2;
        double k = 1.9;
        double q = l * k;
        System.out.println("multiplication: q = " + q);

        // division
        double w = 6.2;
        double dd = 2.1;
        double i = w / dd;
        System.out.println("division: i = " + i);
    }
}
