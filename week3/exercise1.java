public class exercise1 {
    public static void main(String[] args) {
        // addition
        int a = 23;
        int b = 12;
        int c = a + b;
        System.out.println("addition: c = " + c);

        // subtraction
        int g = 26;
        int u = 19;
        int d = g - u;
        System.out.println("subtraction: d = " + d);

        // multiplication
        int l = 2;
        int k = 19;
        int q = l * k;
        System.out.println("multiplication: q = " + q);

        // division
        int w = 6;
        int dd = 2;
        int i = w / dd;
        System.out.println("division: i = " + i);
    }
}
